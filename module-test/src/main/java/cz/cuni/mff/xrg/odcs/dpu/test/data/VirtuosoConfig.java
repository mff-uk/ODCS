package cz.cuni.mff.xrg.odcs.dpu.test.data;

/**
 * Contains configuration that is required in order to connect to virtuoso. 
 * 
 * @author Petyr
 *
 */
public class VirtuosoConfig {
	
	/**
	 * Address of Virtuoso instance.
	 */
	public String host;
	
	/**
	 * Communication port.
	 */
	public String port;
	
	/**
	 * User name used to login to the Virtuoso.
	 */
	public String user;
	
	/**
	 * Password for given Virtuoso.
	 */
	public String password;
	
}
